export const ArithmeticFunction = {
    add: function (a,b) {
        return a+b;
    },
    subtract: function (a,b) {
        return a-b;
    },
    multiply: function (a,b) {
        return a*b;
    },
    divide: function (a,b) {
        return a/b;
    },
    power: function (a) {
        return Math.pow(a,2);
    },
    root: function (a) {
        return Math.sqrt(a);
    }
}